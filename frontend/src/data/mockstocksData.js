// Util to generate 7-day price trend for charting
function generateHistoricalData(currentPrice, symbol) {
  const data = [];
  for (let i = 6; i >= 0; i--) {
    const fluctuation = (Math.random() - 0.5) * 20; // Â±10 fluctuation
    data.push({
      date: new Date(Date.now() - i * 24 * 60 * 60 * 1000).toLocaleDateString(),
      price: +(currentPrice + fluctuation).toFixed(2),
    });
  }
  return data;
}

